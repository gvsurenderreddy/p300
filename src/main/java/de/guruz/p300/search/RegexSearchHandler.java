package de.guruz.p300.search;

import java.util.regex.Matcher;
import java.util.regex.Pattern;

public class RegexSearchHandler extends SearchHandler {
	private Pattern searchPattern;

	@Override
	protected void handleFile(String name, long size) {
//		System.err.println(">handleFile: " + name + ", " + size);
		Matcher m = this.getSearchPattern().matcher(name);
		if (m.find()) {
			this.addSearchResult(name, size);
		}
	}

	public RegexSearchHandler(Pattern p) {
//		System.err.println(">RegexSearchHandler: " + p);
		this.setSearchPattern(p);
	}

	private void setSearchPattern(Pattern searchPattern) {
		this.searchPattern = searchPattern;
	}

	private Pattern getSearchPattern() {
		return this.searchPattern;
	}
}
