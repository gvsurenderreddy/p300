package de.guruz.p300.utils;

/**
 * String utility functions
 * 
 * @author guruz
 *
 */
public class Strings {
	/**
	 * returns the number of characters that are the same at the beginning of both strings
	 * @param a
	 * @param b
	 * @return
	 */
	public static int commonStart(String a, String b) {
		int minLen = (int) MinMax.min(a.length(), b.length());
		int ret = 0;

		for (int i = 0; i < minLen; i++) {
			if (a.charAt(i) == b.charAt(i)) {
				ret++;
			} else {
				break;
			}

		}

		return ret;
	}
}
